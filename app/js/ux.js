function openchat() {
	document.getElementById('chat').style.display = 'block';
  document.getElementById('chat-hide-header').style.display = 'none';
}

function closechat() {
	document.getElementById('chat').style.display = 'none';
}

function hidechat() {
  document.getElementById('chat-hide-header').style.display = 'block';
	document.getElementById('chat').style.display = 'none';
}
